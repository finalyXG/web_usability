  else
   fs=deblank(f);
   if length(fs)>0
    fs=strrep(fs,'.*','*');
    fs=strrep(fs,'./','/');
    fs=strrep(fs,'.^','^');
    fs=strrep(fs,'*','.*');
    fs=strrep(fs,'/','./');
    fs=strrep(fs,'^','.^');
    f=inline(fs);
